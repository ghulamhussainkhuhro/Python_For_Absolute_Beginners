# Polymorphism : Operator overloading 
# When the same operator is allowed to have different meaning according to the context 

# Let's understand the concept of operator overloading

print(1 + 2) # Addition

print("a" + "b") # Concatenation

print([1,2,3] + [1,2,3]) # Merging two lists

print((1,2) + (1,2)) # Merging two tuples

# This overloading is implicit overloading i.e already done by python
# See above, there us only 1 operator but output is different in different situation


# Use for and range() :

# Print numbers from 1 to 100

for i in range (1, 101, 1):
    print(i)

# Print numbers from 100 to 1

for i in range(100, 0, -1):
    print(i)

# Print multiplication of a number 
num = int(input("Enter number you want to print table of : "))
for i in range(1, 11):
    print(num,"x",i,"=",num*i)




"""
 range() 
 range function returns a sequence of numbers starting from 0 by default,
 and increaments by 1 by default, and stops before a specified number

 range(start?, stop, step?)

"""
# Numbers from 0 to 10
print("Counting Numbers")
for i in range(11):
    print(i)

# Even Numbers :
print()
print("Even Numbers:")
for i in range(0,10,2):
    print(i)

# Odd Numbers :
print()
print("Odd Numbers:")
for i in range(1,10,2):
    print(i)

# Multiples of 3
print()
print("Multiples of 3:")
for i in range(3,31,3):
    print(i)

